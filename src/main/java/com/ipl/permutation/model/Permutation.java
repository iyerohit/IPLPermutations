public class Permutation {

	
	private int id;
	private List<Schedule> list;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the list
	 */
	public List<Schedule> getList() {
		return list;
	}
	/**
	 * @param list the list to set
	 */
	public void setList(List<Schedule> list) {
		this.list = list;
	}
	
	
	
	
}
